package me.xuender.audiobooks;

/**
 * @author ender
 * 
 */
public class ResIds {
	private int exitMsg;
	private int layout;
	private int raw;
	private int mainView;
	private int scroll;
	private int scrollText;

	public int getExitMsg() {
		return exitMsg;
	}

	public void setExitMsg(int exitMsg) {
		this.exitMsg = exitMsg;
	}

	public int getLayout() {
		return layout;
	}

	public void setLayout(int layout) {
		this.layout = layout;
	}

	public int getRaw() {
		return raw;
	}

	public void setRaw(int raw) {
		this.raw = raw;
	}

	public int getMainView() {
		return mainView;
	}

	public void setMainView(int mainView) {
		this.mainView = mainView;
	}

	public int getScroll() {
		return scroll;
	}

	public void setScroll(int scroll) {
		this.scroll = scroll;
	}

	public int getScrollText() {
		return scrollText;
	}

	public void setScrollText(int scrollText) {
		this.scrollText = scrollText;
	}
}